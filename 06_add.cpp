// wap to print sum of two number.
#include <iostream>
using namespace std;
int main()
{
    int num1, num2, sum;
    cout << "enter a num1 : ";
    cin >> num1;
    cout << "enter a num2 : ";
    cin >> num2;
    sum = num1 + num2;
    cout << "sum of " << num1 << " and " << num2 << " = " << sum << endl;
    return 0;
}