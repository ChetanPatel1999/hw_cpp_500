// variable example in cpp
#include <iostream>
using namespace std;
int main()
{
    string name = "anirudh maroth";
    int age = 12;
    float height = 5.7;
    char gender = 'm';
    cout << "name of person : " << name << endl;
    cout << "age of person : " << age << " year" << endl;
    cout << "height of person : " << height << " feet" << endl;
    cout << "gender of person : " << gender << endl;
    return 0;
}